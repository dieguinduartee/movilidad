                                                <h5 class="parrafo_noticia"> <p>La Universidad Francisco de Paula Santander a través de la Oficina de Relaciones Interinstitucionales e Internacionales – ORII – dio apertura a la convocatoria para el Programa de Intercambio Académico Latinoamericano (PILA) para el primer semestre de 2022, en modalidad presencial y virtual.</p>

<p>En esta oportunidad y luego de año y medio de restricciones por consecuencia de la pandemia, la UFPS retoma las actividades de movilidad internacional saliente de estudiantes, cumpliendo las normas de bioseguridad y los acuerdos interinstitucionales en el marco de los protocolos establecidos para este proceso.</p>

<p>Nelson Emilio García Torres, director de la ORII, explicó que para el caso de la movilidad presencial se tendrá la posibilidad de postular 4 estudiantes para que realicen su estancia académica en países como Argentina y México.</p>

<p>“En Argentina logramos tener convenio con dos Instituciones Educativas, la Universidad Nacional del Nordeste y la Universidad Nacional del Sur, y en México tendremos la articulación académica con la Universidad Autónoma de Guerrero”, resaltó García Torres.</p>

<p><strong><em>En cuanto a los requisitos para los estudiantes interesados en postularse a la movilidad presencial, el directivo UFPS subrayó los siguientes: </em></strong></p>

<ul style="list-style-type:circle">
	<li>Deberán contar con el esquema de vacunación completo contra el Covid-19.</li>
	<li>Ser estudiante regular de la UFPS en uno de los Programas Académicos señalados en la convocatoria.</li>
	<li>Cumplir con los requisitos internos de movilidad estudiantil internacional&nbsp;establecidos por la institución receptora y la UFPS.</li>
	<li>Presentar un plan de estudios avalado por el Director del Programa Académico de las asignaturas que cursará, y de las asignaturas que le serán&nbsp;homologadas; mínimo tres para las Universidades de Argentina y mínimo&nbsp;cuatro para Universidades de México.</li>
</ul>

<p>En cuanto al escenario de la convocatoria de movilidad virtual, el director de la ORII manifestó que los estudiantes tendrán la posibilidad de cursar asignaturas en Instituciones de Educación Superior de países como Argentina, Brasil, Chile, Cuba, México, Nicaragua y Uruguay.&nbsp;</p>

<p><strong><em>Para este proceso los interesados deberán identificar las posibilidades académicas (asignaturas disponibles) en las Universidades extranjeras a través de la página del Programa PILA: <a href="https://www.programapila.lat/">https://www.programapila.lat/</a></em></strong> ingresando al botón "Acceso a estudiantes - asignaturas para intercambio PILAVirtual".</p>

<p>“Para la movilidad virtual, los estudiantes deberán comunicarse con la oficina de la ORII para postularlos como institución y tener claro los requisitos establecidos para este tipo de estancia académica mediada por las TIC”, informó el directivo UFPS.&nbsp;</p>
